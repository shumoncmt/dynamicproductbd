                                <p>The converter can output up to 5000cp/s pulse series, which is dedicated to external
                                    totalization. Pulse factor is defined as pulse width is selectable from 16ms, 33ms,
                                    66mm, and so on. Photo-coupler isolated transistor open collector circuit is used
                                    for pulse output. The external DC power supply should not exceed 35V and maximum
                                    collector current is 250mA
                                </p>
